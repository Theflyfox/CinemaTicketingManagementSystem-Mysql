package com.suehon.model;

public class FilmOnscreenBean {

	private int showroomId;
	private String filmOnscreenDate;
	private int chooseFilmId;
	private String startTime;
	private String endTime;
	private int filmPrice;

	public int getShowroomId() {
		return showroomId;
	}

	public void setShowroomId(int showroomId) {
		this.showroomId = showroomId;
	}

	public String getFilmOnscreenDate() {
		return filmOnscreenDate;
	}

	public void setFilmOnscreenDate(String filmOnscreenDate) {
		this.filmOnscreenDate = filmOnscreenDate;
	}

	public int getChooseFilmId() {
		return chooseFilmId;
	}

	public void setChooseFilmId(int chooseFilmId) {
		this.chooseFilmId = chooseFilmId;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public int getFilmPrice() {
		return filmPrice;
	}

	public void setFilmPrice(int filmPrice) {
		this.filmPrice = filmPrice;
	}

}
